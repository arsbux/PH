// Counter animation for outcomes section
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    const timer = setInterval(() => {
        start += increment;
        if (start >= target) {
            element.textContent = target;
            clearInterval(timer);
        } else {
            element.textContent = Math.floor(start);
        }
    }, 16);
}

// Observe outcomes section for counter animation
const outcomesObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const counters = entry.target.querySelectorAll('[data-count]');
            counters.forEach(counter => {
                const target = parseInt(counter.getAttribute('data-count'));
                animateCounter(counter, target);
            });
            outcomesObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

const outcomesSection = document.querySelector('.outcomes');
if (outcomesSection) {
    outcomesObserver.observe(outcomesSection);
}

